## Sozlukler - Tanımlama


## Tanımlama - {}

bos_sozluk = {}
kelimeler = {"kitap": "book"}
print(bos_sozluk)               ## output: {}
print(type(bos_sozluk))         ## output: <class 'dict'>
print(kelimeler)                ## output: {'kitap': 'book'}
print(type(kelimeler))          ## output: <class 'dict'>
print(len(kelimeler))
sozluk = {"kitap": "book",
          "bilgisayar": "computer",
          "programlama": "programming",
          "dil": "language",
          "defter": "notebook"}
print(sozluk)                   
